def add_aid(): 
    aid = input("Aid: ")
    aid_quant = input("How many?: ")
    aids[aid] = aid_quant
    print(f"{aid_quant} {aid}s Added!")


def add_misc(): 
    misc = input("Misc: ")
    misc_quant = input("How many?: ")
    miscs[misc] = misc_quant
    print(f"{misc_quant} {misc}s Added!")


def add_ammo(): 
    ammo = input("Ammo: ")
    ammo_quant = input("How many?: ")
    ammos[ammo] = ammo_quant
    print(f"{ammo_quant} {ammo}s Added!")


def drop_weapon(): 
    weapon = input("Weapon: ")
    weapons.remove(weapon)
    print(f"{weapon} Dropped!")


def drop_apparel(): 
    apparel = input("Apparel: ")
    apparels.remove(apparel)
    print(f"{apparel} Dropped!")

def use_aid(): 
    aid = input("Aid: ")
    current_qty = int(aids[aid])
    aid_quant_to_use = int(input("How many?: "))
    if aid_quant_to_use > current_qty:
        print("You don't have enough to use")
    else:
        new_qty = current_qty - aid_quant_to_use
        if new_qty == 0:
            del aids[aid]
            print(f"{aid_quant_to_use} {aid}(s) used!")
        else:
            aids[aid] = str(new_qty)
            print(f"{aid_quant_to_use} {aid}(s) used!")

